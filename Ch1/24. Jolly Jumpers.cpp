#include <iostream>
#include <vector>
using namespace std;
void DistinJellyJump(vector<int> vnArray);
/*
N개의 정수로 이루어진 수열에 대해 서로 인접해 있는 두 수의 차가 1에서 N-1까지의 값을
모두 가지면 그 수열을 유쾌한 점퍼(jolly jumper)라고 부른다. 예를 들어 다음과 같은 수열에
서 1 4 2 3 앞 뒤에 있는 숫자 차의 절대 값이 각각 3 ,2, 1이므로 이 수열은 유쾌한 점퍼가
된다. 어떤 수열이 유쾌한 점퍼인지 판단할 수 있는 프로그램을 작성하라.

▣ 입력설명
첫 번째 줄에 자연수 N(3<=N<=100)이 주어진다.
그 다음 줄에 N개의 정수가 주어진다. 정수의 크기는 int 형 범위안에 있으며, 인접한 두 수의
차도 정수형 범위에 있습니다.

▣ 출력설명
유쾌한 점퍼이면 “YES"를 출력하고, 그렇지 않으면 ”NO"를 출력한다.


*/

int main(void)
{
	freopen("input.txt", "rt", stdin);
	int nSize = 0;
	cin >> nSize;

	vector<int> vnArray(nSize);
	for (int i = 0; i < nSize; ++i)
		cin >> vnArray[i];

	DistinJellyJump(vnArray);
	return 0;
}

void DistinJellyJump(vector<int> vnArray)
{
	//vnArray보다 1 작은 배열을 만들어서 차이의 갯수를 index로 해서 저장함
	vector<int> vnCheckArray(vnArray.size() - 1);
	int nSubtraction = 0;
	int nCounter = 0;

	for (int i = 0; i < vnArray.size() - 1; ++i)
	{
		nSubtraction = abs(vnArray[i] - vnArray[i + 1]);

		//수의 차이가 범위를 벗어나면 더하지 않음 그리고 바로 젤리점프가 아님
		if ((nSubtraction > 0) && (nSubtraction < vnArray.size()))
			vnCheckArray[nSubtraction - 1]++;
		else
		{
			cout << "NO";
			return;
		}
	}

	
	for (int i = 0; i < vnArray.size() - 1; ++i)
	{
		if (vnCheckArray[i] != 1)
		{
			cout << "NO";
			break;
		}
		nCounter++;
	}

	if (nCounter == vnArray.size() - 1)
		cout << "YES";
}