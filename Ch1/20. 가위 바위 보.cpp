#include <iostream>
#define MAXTIME 100
using namespace std;
void RockScissorPaper(const int szA[], const int szB[], const  int nCounterTime);
/*
A, B 두 사람이 가위바위보 게임을 합니다. 총 N번의 게임을 하여 A가 이기면 A를 출력하고,
B가 이기면 B를 출력합니다. 비길 경우에는 D를 출력합니다.
가위, 바위, 보의 정보는 1:가위, 2:바위, 3:보로 정하겠습니다.
예를 들어 N=5이면
회수 1 2 3 4 5
A의 정보 2 3 3 1 3
B의 정보 1 1 2 2 3
승자 A B A B D
두 사람의 각 회의 가위, 바위, 보 정보가 주어지면 각 회를 누가 이겼는지 출력하는 프로그램
을 작성하세요.

첫 번째 줄에 게임 횟수인 자연수 N(1<=N<=100)이 주어집니다.
두 번째 줄에는 A가 낸 가위, 바위, 보 정보가 N개 주어집니다.
세 번째 줄에는 B가 낸 가위, 바위, 보 정보가 N개 주어집니다.

각 줄에 각 회의 승자를 출력합니다. 비겼을 경우는 D를 출력합니다.
*/

int main(void)
{
	int nCounterTime = 0;
	int i = 0;
	int szA[MAXTIME] = { 0 };
	int szB[MAXTIME] = { 0 };

	cin >> nCounterTime;
	for (i = 0; i < nCounterTime; ++i)
	{
		cin >> szA[i];
	}
	for (i = 0; i < nCounterTime; ++i)
	{
		cin >> szB[i];
	}

	RockScissorPaper(szA, szB, nCounterTime);
	return 0;
}

void RockScissorPaper(const int szA[], const int szB[], const  int nCounterTime)
{
	int i = 0;
	for (i = 0; i < nCounterTime; ++i)
	{
		if (szA[i] == szB[i])
		{
			cout << "D" << endl;
			continue;
		}
		if ((szA[i] - szB[i]) == 2)
		{
			cout << "B" << endl;
			continue;
		}

		if ((szA[i] - szB[i]) == -2)
		{
			cout << "A" << endl;
			continue;
		}
		if (szA[i] > szB[i])
		{
			cout << "A" << endl;
			continue;
		}
		
		else
			cout << "B" << endl;
	}

}